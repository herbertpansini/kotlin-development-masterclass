fun main(args: Array<String>) {
    /**
     * If a string contains double quotes, they need to be escaped
     * A character is escaped with a backslash \
     */
    println("The answer is \"yes\" so you can proceed")

    /**
     * If a string contains a backslash, it need to be escaped
     * a double backslash in a string is interpreted as a sigle backslash
     */
    println("I like to put a \\ in my code")
}